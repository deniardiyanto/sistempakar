<p><p style="font-family:'Verdana', Courier, monospace; color:#0000; text-align:justify">
Kucing ini merupakan kucing domestik dengan karakteristik bulu panjang, wajah bulat dengan moncong yang pendek. Selain itu, ia juga memiliki sifat pemalas dan manja. Sesuai dengan namanya, ia berasal dari Persia yang mana merupakan nama lama dari Iran. Kucing hanya mampu menjilati bagian yang sakit dari luar saja, jika penyakit yang menyerang berada di dalam tentu kucing tidak dapat mengobati sendiri. Berikut adalah beberapa penyakit yang menyerang kucing.</p></p>
<p><p style="font-family:'Verdana', Courier, monospace; color:#0000; text-align:justify"></br>Kucing Persia adalah salah satu ras kucing yang disukai oleh para pecinta kucing. Sebagai hewan karnivora kucing juga memiliki insting dan naluri berburu yang cukup tinggi, dengan memburu hewan pengerat yang lebih kecil dari ukuran kucing seperti tikus.</p>

