Pengertian umum diagnosa adalah penentuan jenis penyakit dengan meneliti (memeriksa) gejala-gajalanya. Dari segi medis, diagnosa adalah proses penentuan jenis penyakit berdasarkan tanda dan gejala dengan menggunakan cara dan alat seperti laboratorium, foto dan klinik. Penyakit adalah gangguan pada bagian tubuh, atau gangguan kesehatan. Dalam sistem pakar ini peneliti menggunakan penalaran berbasis aturan (Rule Based Reasoning). Pada penalaran berbasis aturan, representasi menggunakan IF-THEN, bentuk ini digunakan apabila kita memiliki sejumlah pengetahuan seorang pakar pada suatu permasalahan tertentu dan pakar dapat menyelesaikan masalah tersebut secara berurutan
